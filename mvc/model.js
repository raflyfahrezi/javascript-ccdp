class Student {
    constructor() {
        this.name
        this.age
        this.id
    }

    setName(name) {
        this.name = name
    }

    getName() {
        return this.name
    }

    setAge(age) {
        this.age = age
    }

    getAge() {
        return this.age
    }

    setId(id) {
        this.id = id
    }

    getId() {
        return this.id
    }
}

module.exports = Student
