class Student {
    constructor() {
        this.name
        this.year
        this.id
    }

    setName(name) {
        this.name = name
    }

    getName() {
        return this.name
    }

    setYear(year) {
        this.year = year
    }

    getYear() {
        return this.year
    }

    setId(id) {
        this.id = id
    }

    getId() {
        return this.id
    }
}

module.exports = Student
